// We use these three functions to print output messages in Go programming.

// fmt.Print()
// fmt.Println()
// fmt.Printf()

// Note: All these functions are defined under the fmt package. 
// So, we must import the fmt package before we can use these functions.

// Program to illustrate fmt.Print() 

package main

// import fmt package
import "fmt"

func main() {

  fmt.Print("Hello, ")
  fmt.Print("World!")

}